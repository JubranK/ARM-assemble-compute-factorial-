PRESERVE8
THUMB
INITIAL_MSP EQU 0x20001000

AREA RESET, DATA, READONLY
EXPORT __Vectors
__Vectors DCD INITIAL_MSP
	DCD Reset_Handler
	ALIGN

AREA MYCODE, CODE, READONLY
ENTRY
EXPORT Reset_Handler
ALIGN

FACTORIAL PROC
	
LOOP
    MUL R0,R0,R1
    SUB R1, R1, #1
    CMP R1, #0
    BEQ ENDD
    B LOOP

ZEROFACTORIAL
    MOV R0, #1
	
    ENDP

Reset_Handler
    MOV R1, #5
    MOV R0, #1

    MOV R2, #0
    CMP R1, R2
    BEQ ZEROFACTORIAL
    BL FACTORIAL

ENDD

STOP
    B STOP

    END

    B STOP

    END
